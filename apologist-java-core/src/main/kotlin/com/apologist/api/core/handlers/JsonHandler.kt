@file:JvmName("JsonHandler")

package com.apologist.api.core.handlers

import com.apologist.api.core.http.HttpResponse
import com.apologist.api.core.http.HttpResponse.Handler
import com.apologist.api.errors.ApologistInvalidDataException
import com.fasterxml.jackson.databind.json.JsonMapper
import com.fasterxml.jackson.module.kotlin.jacksonTypeRef

@JvmSynthetic
internal inline fun <reified T> jsonHandler(jsonMapper: JsonMapper): Handler<T> =
    object : Handler<T> {
        override fun handle(response: HttpResponse): T =
            try {
                jsonMapper.readValue(response.body(), jacksonTypeRef())
            } catch (e: Exception) {
                throw ApologistInvalidDataException("Error reading response", e)
            }
    }
